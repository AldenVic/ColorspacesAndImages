
public class CTripletDouble {

	private final double _a;
	private final double _b;
	private final double _c;

	public CTripletDouble(double a, double b, double c) {
		_a = a;
		_b = b;
		_c = c;
	}

	public double getA() {
		return _a;
	}

	public double getB() {
		return _b;
	}

	public double getC() {
		return _c;
	}

}
